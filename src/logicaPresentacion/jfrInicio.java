    private void btnSesionGerenteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSesionGerenteActionPerformed

        String login, password;
        login = usuario.getText();
        password = new String(pass.getPassword());
        Persona index = con.autenticar(login, password);
        if(index == null) JOptionPane.showMessageDialog(null, "Revisa tus datos");
        else if(index.getClass().equals(Gerente.class)){
            jfrFuncionesGerente abrir = new jfrFuncionesGerente();
            abrir.setVisible(true);
            this.setVisible(false);
        }
        else if(index.getClass().equals(Empleado.class)){
            jfrFuncionesVendedor abrir = new jfrFuncionesVendedor((Empleado) index);
            abrir.setVisible(true);
            this.setVisible(false);
        }
        else{
            JOptionPane.showMessageDialog(null, "Revisa tus datos");
        }
        
// TODO add your handling code here:
    }//GEN-LAST:event_btnSesionGerenteActionPerformed

