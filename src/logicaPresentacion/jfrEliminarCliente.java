    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String query;
        query = fieldBuscar.getText();
        Cliente cliente = db.findCliente(query);
        if(cliente == null){
            areaMostrar.setText("");
            JOptionPane.showMessageDialog(null, "No se encontraron coincidencias");
            id = -1;
        }
        else{
            areaMostrar.setText(cliente.imprimir());
            id = cliente.getId();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(id != -1){
            if(db.deleteCliente(id)){
                areaMostrar.setText("");
                JOptionPane.showMessageDialog(null, "Eliminado");
            }
            else JOptionPane.showMessageDialog(null, "No se pudo eliminar");
        }
        else JOptionPane.showMessageDialog(null, "Seleccione un cliente");
    }//GEN-LAST:event_jButton2ActionPerformed

