    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        // TODO add your handling code here:
        setBtns(false);
        String nombre, codigo;
        float costo, precio;
        
        nombre = fieldNombre.getText();
        codigo = fieldCodigo.getText();
        if(!nombre.equals("") && !codigo.equals("")){
            try{
                costo = Float.valueOf(fieldCosto.getText());
                precio = Float.valueOf(fieldPrecio.getText());
                Producto producto = new Producto(nombre, codigo, 0, costo, precio);
                if(pro.insertProducto(producto)){
                    limpiarCampos();
                    JOptionPane.showMessageDialog(null, "Creado");
                    generar();
                }
                else JOptionPane.showMessageDialog(null, "No se crear");
            }
            catch(NumberFormatException e){
                JOptionPane.showMessageDialog(null, "El costo y precio deben ser numericos");
            }
        }
        else JOptionPane.showMessageDialog(null, "Todos los campos son obligatorios");
    }//GEN-LAST:event_btnCrearActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String query;
        query = fieldCodigo.getText();
        Producto producto = pro.findProducto(query);
        if(producto != null){
            fieldNombre.setText(producto.getNombre()) ;
            fieldPrecio.setText(String.valueOf(producto.getPrecio()));
            fieldCosto.setText(String.valueOf(producto.getCosto()));
            id = producto.getId();
            setBtns(true);
       }
        else {
            JOptionPane.showMessageDialog(null, "No se encontraron coincidencias");
            limpiarCampos();
            setBtns(false);
            id = -1;
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        if(id != -1){
            String nombre, codigo;
            float costo, precio;
        
            nombre = fieldNombre.getText();
            codigo = fieldCodigo.getText();
            if(!nombre.equals("") && !codigo.equals("")){
                try{
                    costo = Float.valueOf(fieldCosto.getText());
                    precio = Float.valueOf(fieldPrecio.getText());
                    Producto producto = new Producto(nombre, codigo, 0, costo, precio);
                    if(pro.editProduct(id,producto)){
                        limpiarCampos();
                        JOptionPane.showMessageDialog(null, "Editado");
                        generar();
                    }
                    else JOptionPane.showMessageDialog(null, "No se editar");
                }
                catch(NumberFormatException e){
                    JOptionPane.showMessageDialog(null, "El costo y precio deben ser numericos");
                }
            }
            else JOptionPane.showMessageDialog(null, "Todos los campos son obligatorios");
        }
        else  JOptionPane.showMessageDialog(null, "Escoga un producto");
    }//GEN-LAST:event_btnEditarActionPerformed

