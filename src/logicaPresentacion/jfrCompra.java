    public void generar(){
        Producto producto;
        TableModel modelo = tabla.getModel();
        compras = new ArrayList<>();
        Compra compra;
        float total = 0;
        int cantidad;
        String x, noEncontrados = "";
        for(int i=0; i<tabla.getRowCount(); i++){
            x = (String) modelo.getValueAt(i, 1);
            producto = pro.findProducto(x);
            if(producto != null){
                compra = new Compra();
                compra.setFacCompra(factura);
                compra.setProducto(producto);
                cantidad = (int) modelo.getValueAt(i,3);
                compra.setCantidad(cantidad);
                compra.setTotal(cantidad*producto.getCosto());
                compras.add(compra);
                modelo.setValueAt(producto.getNombre(), i, 0);
                modelo.setValueAt(producto.getCosto(), i, 2);
                modelo.setValueAt(compra.getTotal(), i, 4);
                total = total + compra.getTotal();
            }
            else{
                if(x != null)  noEncontrados = noEncontrados+" "+x;
            }
        }
        txtTotal.setText(String.valueOf(total));
        factura.setTotal(total);
        if(!noEncontrados.equals("")){
            JOptionPane.showMessageDialog(null, "Las referencias "+noEncontrados+" no existen en el sistema");
        }
    }
